const mongoose = require("mongoose");

let productSchema = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  discountPercentage: {
    type: Number,
    required: false,
  },
  rating: {
    type: Number,
    required: false,
  },
  stock: {
    type: Number,
    required: false,
  },
  brand: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  thumbnail: {
    type: String,
    required: true,
  },
  images: {
    type: [String],
    required: true,
  },
});

let Product = (module.exports = mongoose.model("Product", productSchema));
